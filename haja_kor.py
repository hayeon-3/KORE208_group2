def convert_hanja_to_korean(text, dictionary):
    # 按汉字长度从长到短排序，优先替换多字词
    sorted_hanja = sorted(dictionary.keys(), key=lambda x: -len(x))
    for hanja in sorted_hanja:
        text = text.replace(hanja, dictionary[hanja])
    return text

def process_file(input_file, output_file):
    # 读取输入文件
    with open(input_file, 'r', encoding='utf-8') as f:
        content = f.read()
    
    # 转换汉字为韩文
    converted_content = convert_hanja_to_korean(content, hanja_to_korean)
    
    # 写入输出文件
    with open(output_file, 'w', encoding='utf-8') as f:
        f.write(converted_content)
    
    print(f"文件处理完成，结果已保存到 {output_file}")

